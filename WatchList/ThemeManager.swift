import Foundation
import UIKit

extension UIColor {
    
    static func darkBackgroundColor() -> UIColor {
        return UIColor(red: 32.0 / 255.0, green: 33.0 / 255.0, blue: 37.0 / 255.0, alpha: 1.0)
    }
    
}
